// Función para mostrar el pop-up
function showPopUp1() {
    // Obtiene el elemento del pop-up por su ID
    var popUp = document.getElementById("popUpBox1");
    // Cambia la propiedad display a block para hacerlo visible
    popUp.style.display = "block";

}

// Función para ocultar el pop-up
function hidePopUp1() {
    // Obtiene el elemento del pop-up por su ID
    var popUp = document.getElementById("popUpBox1");
    // Cambia la propiedad display a none para hacerlo invisible
    popUp.style.display = "none";
}

